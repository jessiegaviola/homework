/*
 *    Sum of Two Numbers
 *    
 * Write a program that stores the integers 62 and 99
 * in variables and stores the sum of these two in a
 * variable named total. Display the total on the screen
 */

#include <iostream>

using namespace std;

int main()
{
    int num1, num2, total; // will store integers 62 and 99 in variables
    
    num1 = 62, num2 = 99;
    total = num1 + num2;
    
    cout << total << endl;

    return 0;
}

